import math # this will import the MATH library so we can use its functions

pi = 3.14

print(round(pi)) # rounds the number
print(math.ceil(pi)) # rounds the number up
print(math.floor(pi)) # rounds the number down
print() # prints an empty line

print (abs(pi)) # prints the absolute value of the number
print (pow(pi,3)) # prints pi to the power of 3
print (math.sqrt(pi)) # prints the square root
print() # prints an empty line

